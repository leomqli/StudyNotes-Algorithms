/*
 
 算法题的解题步骤：
 
 1. 充分阅读题目，了解题目背后的关键意思；
 2. 分析题目，涉及到哪些数据结构，对问题进行分类。
	判断到底属于哪类问题（链表问题、栈思想问题、字符串问题、二叉树问题、图相关问题、排序问题），
	与你之前所接触过的算法题有没有类似，找到问题的解题思路；
 3. 实现算法。在算法的实现的过程，并不是一蹴而就，需要不断的调试、修改；
 4. 验证算法正确性；
 5. 找到题源，看其他的开发者对齐的解决思路；
 6. 找到题解建议之后，对于其他优秀思路，分析它的优势，并且学习它的思路，写成其他解法的代码；
 7. 算法题的解题能力来自于3点：夯实的数据结构与算法基础 + 足够多的积累 + 足够多的耐心。

*/
